    var status_CL_prob = 0.3;
    var status_D_prob = 0.4;

    // Unused variable used to impact prediction
    var ageFactor = Age / 10000;
    status_C_prob -= ageFactor;
    status_CL_prob += ageFactor;

    if (Bilirubin >= 2.0) {
        status_C_prob = 0.1;
        status_CL_prob = 0.1;
        status_D_prob = 0.8;
    } else if (Bilirubin < 1.0) {
        status_C_prob = 0.5;
        status_CL_prob = 0.3;
        status_D_prob = 0.2;
    }

